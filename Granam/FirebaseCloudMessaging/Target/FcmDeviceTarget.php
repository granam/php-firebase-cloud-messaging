namespace Granam\FirebaseCloudMessaging\Target;

class FcmDeviceTarget extends FcmTarget
{
    private $token;

    public function __construct(string $token)
    {
        $this->token = $token;
    }

    public function getToken(): string
    {
        return $this->token;
    }
}